
public class IntegerTypes {
    public static void main(String[] args) {
        byte b = 1;
        System.out.println(b); // 1
        int i = 5;
        System.out.println(i); // 5
        long num = 3L;
        System.out.println(num); // 3
        short sum = (short) (b + i + num);
        System.out.println(sum); // 9
    }
}
